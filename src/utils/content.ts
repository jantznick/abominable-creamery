// Function to generate simple slugs
const generateSlug = (title: string): string => {
	return title
		.toLowerCase()
		.replace(/\s+/g, '-') // Replace spaces with hyphens
		.replace(/[^Ѐ-ӿ\w-]+/g, '') // Remove non-alphanumeric (allow cyrillic), non-hyphen
		.replace(/--+/g, '-') // Replace multiple hyphens with single
		.replace(/^-+/, '') // Trim hyphen from start
		.replace(/-+$/, ''); // Trim hyphen from end
};

// Define content block types
type ParagraphBlock = { type: 'paragraph'; text: string };
type ListBlock = { type: 'list'; items: string[] };
export type ContentBlock = ParagraphBlock | ListBlock;

// Define the raw article structure type before adding slug/summary
